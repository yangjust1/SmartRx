void getAngle(float* angle, float* rot, float* prev, float* diff, float* num, uint8_t* init) { // Updates total rotation
	float current_position = *angle;

    // calculate the rotation from the previous position to the current position
	*diff = current_position - *prev;

	if (*diff < -180 && !(*init)) { // Wrap Around
		++(*num);
	}

	else if (*diff > 180 && !(*init)) {// Wrap Around
		--(*num);
	}

	*rot = *angle + (*num) * 360.0;

    // update the previous position to the current position
	*prev = current_position;
	*init = 0;
}

